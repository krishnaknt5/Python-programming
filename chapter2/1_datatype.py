# about primary datatypes in python
a=1 #a is an integer
b=2.5 #b is a float
c="Hello" #c is a string
d=True #d is a boolean
e=None #e is a NoneType
f=[1, 2, 3] #f is a list
g=(1, 2, 3) #g is a tuple
h={'key': 'value'} #h is a dictionary
i=set([1, 2, 3]) #i is a set
j=frozenset([1, 2, 3]) #j is a frozenset
k=bytearray(5) #k is a bytearray
l=bytes(5) #l is a bytes object
m=memoryview(bytes(5)) #m is a memoryview
n=complex(1, 2) #n is a complex number
o=range(5) #o is a range object
p=slice(1, 5) #p is a slice object
q=type(a) #q is the type of a
print('a',b,c,d,e,f,g,h,i,j,k,l,m,n,o,p,q) # printing all the variables